function autoFontFaceDependencyGraphProofCandidates({ sourcePath, input = {}, dependencyGraphEvidence, sourceShapeChanges = [], outputDependencyGraphEvidence, binding, hash }) {
  if (input.cssDependencyGraphAutoProofs === false || input.autoCssDependencyGraphProofs === false) return [];
  const result = [];
  for (const side of ['worker', 'head']) {
    const opposite = side === 'worker' ? 'head' : 'worker';
    const baseEvidence = dependencyGraphEvidence.sides?.base;
    const sideEvidence = dependencyGraphEvidence.sides?.[side];
    const oppositeEvidence = dependencyGraphEvidence.sides?.[opposite];
    if (!baseEvidence || !sideEvidence || !oppositeEvidence || !outputDependencyGraphEvidence) continue;
    const renames = fontFaceRenames(baseEvidence, sideEvidence);
    if (renames.length !== 1) continue;
    const rename = renames[0];
    if (!oppositePreservesFontDependencyGraph(baseEvidence, oppositeEvidence, rename)) continue;
    const changedSurfaces = (dependencyGraphEvidence.changedDependencySurfaces ?? [])
      .filter((change) => change.side === side && fontFamilyChangeMatchesRename(change, rename));
    if (!changedSurfaces.length) continue;
    if (!allBaseFontLinksMoved(baseEvidence, sideEvidence, rename)) continue;
    if (!outputPreservesFontFaceRename(sideEvidence, outputDependencyGraphEvidence, rename)) continue;
    const coveredShapes = fontFaceRenameShapeChanges(sourceShapeChanges, side, rename);
    if (!coveredShapes.length) continue;
    result.push(compactRecord({
      id: `auto_css_font_face_family_${side}_${cssIdentifier(rename.from)}_to_${cssIdentifier(rename.to)}`,
      kind: 'css-source-bound-dependency-graph-proof',
      status: 'passed',
      proofLevel: 'css-font-face-family-source-bound',
      sourcePath,
      reasonCode: 'css-dependency-graph-proof-unproved',
      side,
      cascadeKeys: changedSurfaces.map((change) => change.cascadeKey),
      ...sourceHashRecord(binding, hash),
      dependencyGraphHashes: compactRecord({
        base: baseEvidence.dependencyGraphHash,
        worker: dependencyGraphEvidence.sides?.worker?.dependencyGraphHash,
        head: dependencyGraphEvidence.sides?.head?.dependencyGraphHash,
        output: outputDependencyGraphEvidence.dependencyGraphHash
      }),
      outputDependencyGraphHash: outputDependencyGraphEvidence.dependencyGraphHash,
      autoGenerated: true,
      sourceBound: true,
      shapeKeys: coveredShapes.map((change) => change.shapeKey),
      coveredSourceShapeChanges: coveredShapes.map(sourceShapeChangeKey),
      fontFaceRename: {
        from: rename.from,
        to: rename.to,
        normalizedBlockHash: rename.normalizedBlockHash,
        declarationCascadeKeys: changedSurfaces.map((change) => change.cascadeKey),
        shapeKeys: coveredShapes.map((change) => change.shapeKey),
        baseFontReferenceCount: fontLinksForFamily(baseEvidence, rename.from).length,
        outputFontReferenceCount: fontLinksForFamily(outputDependencyGraphEvidence, rename.to).length,
        browserCascadeEquivalenceClaim: false,
        browserRenderEquivalenceClaim: false,
        semanticEquivalenceClaim: false
      }
    }));
  }
  return result;
}

function fontFaceRenames(baseEvidence, sideEvidence) {
  const baseFaces = fontFacesByFamily(baseEvidence);
  const sideFaces = fontFacesByFamily(sideEvidence);
  const deleted = [...baseFaces.values()].filter((entry) => !sideFaces.has(entry.family));
  const added = [...sideFaces.values()].filter((entry) => !baseFaces.has(entry.family));
  if (!deleted.length || deleted.length !== added.length) return [];
  return deleted.flatMap((before) => {
    const matches = added.filter((after) => before.normalizedBlockHash && before.normalizedBlockHash === after.normalizedBlockHash);
    if (matches.length !== 1) return [];
    const after = matches[0];
    return [{ from: before.family, to: after.family, normalizedBlockHash: before.normalizedBlockHash, before, after }];
  });
}

function fontFacesByFamily(evidence) {
  return new Map((evidence.records?.fontFaces ?? []).filter((entry) => entry.family).map((entry) => [entry.family, entry]));
}

function oppositePreservesFontDependencyGraph(baseEvidence, oppositeEvidence, rename) {
  return sameFontFaces(baseEvidence, oppositeEvidence) &&
    sameFontLinks(baseEvidence, oppositeEvidence) &&
    !fontFacesByFamily(baseEvidence).has(rename.to);
}

function sameFontFaces(left, right) {
  return stableList((left.records?.fontFaces ?? []).map((entry) => `${entry.family}:${entry.normalizedBlockHash}`)) ===
    stableList((right.records?.fontFaces ?? []).map((entry) => `${entry.family}:${entry.normalizedBlockHash}`));
}

function sameFontLinks(left, right) {
  return stableList((left.records?.fontFaceLinks ?? []).map(fontLinkIdentity)) ===
    stableList((right.records?.fontFaceLinks ?? []).map(fontLinkIdentity));
}

function fontFamilyChangeMatchesRename(change, rename) {
  if (change.property !== 'font-family') return false;
  if (!change.before?.dependencyKinds?.includes('font-face-link') || !change.after?.dependencyKinds?.includes('font-face-link')) return false;
  if (!sameSet(change.before.names, [rename.from]) || !sameSet(change.after.names, [rename.to])) return false;
  return fontFamilyValueRenamedOnly(change.beforeValue, change.afterValue, rename.from, rename.to);
}

function allBaseFontLinksMoved(baseEvidence, sideEvidence, rename) {
  const baseLinks = fontLinksForFamily(baseEvidence, rename.from);
  if (!baseLinks.length) return false;
  if (fontLinksForFamily(sideEvidence, rename.from).length) return false;
  const sideLinks = fontLinksForFamily(sideEvidence, rename.to);
  return stableList(baseLinks.map(fontLinkWithoutFamilyIdentity)) === stableList(sideLinks.map(fontLinkWithoutFamilyIdentity)) &&
    sideLinks.every((entry) => entry.targetDefined === true);
}

function outputPreservesFontFaceRename(sideEvidence, outputEvidence, rename) {
  const outputFaces = fontFacesByFamily(outputEvidence);
  const sideFaces = fontFacesByFamily(sideEvidence);
  if (!outputFaces.has(rename.to) || outputFaces.has(rename.from)) return false;
  if (outputFaces.get(rename.to)?.normalizedBlockHash !== sideFaces.get(rename.to)?.normalizedBlockHash) return false;
  if (fontLinksForFamily(outputEvidence, rename.from).length) return false;
  return stableList(fontLinksForFamily(sideEvidence, rename.to).map(fontLinkWithoutFamilyIdentity)) ===
    stableList(fontLinksForFamily(outputEvidence, rename.to).map(fontLinkWithoutFamilyIdentity));
}

function fontFaceRenameShapeChanges(sourceShapeChanges, side, rename) {
  return sourceShapeChanges.filter((change) => change.side === side &&
    change.before?.atRuleName === 'font-face' &&
    change.after?.atRuleName === 'font-face' &&
    change.before?.rawTextHash !== change.after?.rawTextHash &&
    change.reasonCode === 'css-font-face-runtime-equivalence-unproved');
}

function fontLinksForFamily(evidence, family) {
  return (evidence.records?.fontFaceLinks ?? []).filter((entry) => entry.family === family);
}

function fontLinkIdentity(entry) {
  return [entry.cascadeKey, entry.property, entry.family, entry.declarationHash].join('::');
}

function fontLinkWithoutFamilyIdentity(entry) {
  return [entry.cascadeKey, entry.property].join('::');
}

function fontFamilyValueRenamedOnly(beforeValue, afterValue, from, to) {
  if (typeof beforeValue !== 'string' || typeof afterValue !== 'string') return false;
  return normalizeCssValue(replaceCssFamily(beforeValue, from, to)) === normalizeCssValue(afterValue);
}

function replaceCssFamily(value, from, to) {
  const escaped = String(from).replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  return String(value).replace(new RegExp(`(^|[\\s,])(\"?)${escaped}\\2(?=$|[\\s,])`, 'g'), `$1$2${to}$2`);
}

function sourceHashRecord(binding = {}, hash) {
  return compactRecord({
    baseSourceHash: hash?.(binding.base),
    workerSourceHash: hash?.(binding.worker),
    headSourceHash: hash?.(binding.head),
    outputSourceHash: hash?.(binding.output)
  });
}

function sourceShapeChangeKey(change) {
  return [change.side, change.reasonCode, change.shapeKey].join('::');
}

function sameSet(left = [], right = []) { return stableList(left) === stableList(right); }
function stableList(values = []) { return [...new Set(values.filter(Boolean))].sort().join('\u0000'); }
function normalizeCssValue(value) { return String(value ?? '').trim().replace(/\s+/g, ' '); }
function cssIdentifier(value) { return String(value ?? 'unknown').replace(/[^A-Za-z0-9_-]/g, '-').replace(/^-+/, '') || 'unknown'; }
function compactRecord(record) { return Object.fromEntries(Object.entries(record).filter(([, value]) => value !== undefined)); }

export { autoFontFaceDependencyGraphProofCandidates };
