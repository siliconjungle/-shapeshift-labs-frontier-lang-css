function autoDependencyGraphProofCandidates({ sourcePath, input = {}, dependencyGraphEvidence, sourceShapeChanges = [], outputDependencyGraphEvidence, binding, hash }) {
  if (input.cssDependencyGraphAutoProofs === false || input.autoCssDependencyGraphProofs === false) return [];
  return keyframesAnimationNameAutoProofs({ sourcePath, dependencyGraphEvidence, sourceShapeChanges, outputDependencyGraphEvidence, binding, hash });
}

function keyframesAnimationNameAutoProofs({ sourcePath, dependencyGraphEvidence, sourceShapeChanges = [], outputDependencyGraphEvidence, binding, hash }) {
  const result = [];
  for (const side of ['worker', 'head']) {
    const opposite = side === 'worker' ? 'head' : 'worker';
    const sideEvidence = dependencyGraphEvidence.sides?.[side];
    const baseEvidence = dependencyGraphEvidence.sides?.base;
    const oppositeEvidence = dependencyGraphEvidence.sides?.[opposite];
    if (!baseEvidence || !sideEvidence || !oppositeEvidence || !outputDependencyGraphEvidence) continue;
    const renames = keyframeRenames(baseEvidence, sideEvidence);
    if (renames.length !== 1) continue;
    const rename = renames[0];
    if (!oppositePreservesKeyframeDependencyGraph(baseEvidence, oppositeEvidence, rename)) continue;
    const changedSurfaces = (dependencyGraphEvidence.changedDependencySurfaces ?? [])
      .filter((change) => change.side === side && animationNameChangeMatchesRename(change, rename));
    if (!changedSurfaces.length) continue;
    if (!allBaseAnimationLinksMoved(baseEvidence, sideEvidence, rename)) continue;
    if (!outputPreservesKeyframeRename(sideEvidence, outputDependencyGraphEvidence, rename)) continue;
    const coveredShapes = keyframeRenameShapeChanges(sourceShapeChanges, side, rename);
    if (coveredShapes.length < 2) continue;
    result.push(compactRecord({
      id: `auto_css_keyframes_animation_name_${side}_${cssIdentifier(rename.from)}_to_${cssIdentifier(rename.to)}`,
      kind: 'css-source-bound-dependency-graph-proof',
      status: 'passed',
      proofLevel: 'css-keyframes-animation-name-source-bound',
      sourcePath,
      reasonCode: 'css-dependency-graph-proof-unproved',
      side,
      cascadeKeys: changedSurfaces.map((change) => change.cascadeKey),
      ...sourceHashRecord(binding, hash),
      dependencyGraphHashes: compactRecord({
        base: baseEvidence.dependencyGraphHash,
        worker: dependencyGraphEvidence.sides?.worker?.dependencyGraphHash,
        head: dependencyGraphEvidence.sides?.head?.dependencyGraphHash,
        output: outputDependencyGraphEvidence.dependencyGraphHash
      }),
      outputDependencyGraphHash: outputDependencyGraphEvidence.dependencyGraphHash,
      autoGenerated: true,
      sourceBound: true,
      shapeKeys: coveredShapes.map((change) => change.shapeKey),
      coveredSourceShapeChanges: coveredShapes.map(sourceShapeChangeKey),
      keyframeRename: {
        from: rename.from,
        to: rename.to,
        bodyHash: rename.bodyHash,
        declarationCascadeKeys: changedSurfaces.map((change) => change.cascadeKey),
        shapeKeys: coveredShapes.map((change) => change.shapeKey),
        baseAnimationReferenceCount: animationLinksForName(baseEvidence, rename.from).length,
        outputAnimationReferenceCount: animationLinksForName(outputDependencyGraphEvidence, rename.to).length,
        browserCascadeEquivalenceClaim: false,
        browserRenderEquivalenceClaim: false,
        semanticEquivalenceClaim: false
      }
    }));
  }
  return result;
}

function keyframeRenames(baseEvidence, sideEvidence) {
  const baseKeyframes = keyframesByName(baseEvidence);
  const sideKeyframes = keyframesByName(sideEvidence);
  const deleted = [...baseKeyframes.values()].filter((entry) => !sideKeyframes.has(entry.name));
  const added = [...sideKeyframes.values()].filter((entry) => !baseKeyframes.has(entry.name));
  if (!deleted.length || deleted.length !== added.length) return [];
  return deleted.flatMap((before) => {
    const matches = added.filter((after) => before.bodyHash && before.bodyHash === after.bodyHash);
    if (matches.length !== 1) return [];
    const after = matches[0];
    return [{ from: before.name, to: after.name, bodyHash: before.bodyHash, before, after }];
  });
}

function keyframesByName(evidence) {
  return new Map((evidence.records?.keyframes ?? []).filter((entry) => entry.name).map((entry) => [entry.name, entry]));
}

function oppositePreservesKeyframeDependencyGraph(baseEvidence, oppositeEvidence, rename) {
  return sameKeyframeNames(baseEvidence, oppositeEvidence) &&
    sameAnimationNameLinks(baseEvidence, oppositeEvidence) &&
    !keyframesByName(baseEvidence).has(rename.to);
}

function sameKeyframeNames(left, right) {
  return stableList(left.records?.keyframes?.map((entry) => `${entry.name}:${entry.bodyHash}`)) ===
    stableList(right.records?.keyframes?.map((entry) => `${entry.name}:${entry.bodyHash}`));
}

function sameAnimationNameLinks(left, right) {
  return stableList((left.records?.animationNameLinks ?? []).map(animationLinkIdentity)) ===
    stableList((right.records?.animationNameLinks ?? []).map(animationLinkIdentity));
}

function animationNameChangeMatchesRename(change, rename) {
  if (change.property !== 'animation-name') return false;
  if (!change.before?.dependencyKinds?.includes('animation-name-link') || !change.after?.dependencyKinds?.includes('animation-name-link')) return false;
  if (!sameSet(change.before.names, [rename.from]) || !sameSet(change.after.names, [rename.to])) return false;
  return animationNameValueRenamedOnly(change.beforeValue, change.afterValue, rename.from, rename.to);
}

function allBaseAnimationLinksMoved(baseEvidence, sideEvidence, rename) {
  const baseLinks = animationLinksForName(baseEvidence, rename.from);
  if (!baseLinks.length) return false;
  if (animationLinksForName(sideEvidence, rename.from).length) return false;
  const sideLinks = animationLinksForName(sideEvidence, rename.to);
  return stableList(baseLinks.map(animationLinkWithoutNameIdentity)) === stableList(sideLinks.map(animationLinkWithoutNameIdentity)) &&
    sideLinks.every((entry) => entry.targetDefined === true);
}

function outputPreservesKeyframeRename(sideEvidence, outputEvidence, rename) {
  const outputKeyframes = keyframesByName(outputEvidence);
  const sideKeyframes = keyframesByName(sideEvidence);
  if (!outputKeyframes.has(rename.to) || outputKeyframes.has(rename.from)) return false;
  if (outputKeyframes.get(rename.to)?.bodyHash !== sideKeyframes.get(rename.to)?.bodyHash) return false;
  if (animationLinksForName(outputEvidence, rename.from).length) return false;
  return stableList(animationLinksForName(sideEvidence, rename.to).map(animationLinkWithoutNameIdentity)) ===
    stableList(animationLinksForName(outputEvidence, rename.to).map(animationLinkWithoutNameIdentity));
}

function keyframeRenameShapeChanges(sourceShapeChanges, side, rename) {
  const changes = sourceShapeChanges.filter((change) => change.side === side && (
    (change.before?.atRuleName === 'keyframes' && change.before?.conditionText === rename.from && !change.after) ||
    (change.after?.atRuleName === 'keyframes' && change.after?.conditionText === rename.to && !change.before)
  ));
  const hasDelete = changes.some((change) => change.before?.conditionText === rename.from && !change.after);
  const hasAdd = changes.some((change) => change.after?.conditionText === rename.to && !change.before);
  return hasDelete && hasAdd ? changes : [];
}

function animationLinksForName(evidence, name) {
  return (evidence.records?.animationNameLinks ?? []).filter((entry) => entry.name === name);
}

function animationLinkIdentity(entry) {
  return [entry.cascadeKey, entry.property, entry.name, entry.declarationHash].join('::');
}

function animationLinkWithoutNameIdentity(entry) {
  return [entry.cascadeKey, entry.property].join('::');
}

function animationNameValueRenamedOnly(beforeValue, afterValue, from, to) {
  if (typeof beforeValue !== 'string' || typeof afterValue !== 'string') return false;
  return normalizeCssValue(replaceCssIdentifier(beforeValue, from, to)) === normalizeCssValue(afterValue);
}

function replaceCssIdentifier(value, from, to) {
  const escaped = String(from).replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  return String(value).replace(new RegExp(`(^|[^-A-Za-z0-9_])${escaped}(?=$|[^-A-Za-z0-9_])`, 'g'), `$1${to}`);
}

function sourceHashRecord(binding = {}, hash) {
  return compactRecord({
    baseSourceHash: hash?.(binding.base),
    workerSourceHash: hash?.(binding.worker),
    headSourceHash: hash?.(binding.head),
    outputSourceHash: hash?.(binding.output)
  });
}

function sourceShapeChangeKey(change) {
  return [change.side, change.reasonCode, change.shapeKey].join('::');
}

function sameSet(left = [], right = []) { return stableList(left) === stableList(right); }
function stableList(values = []) { return [...new Set(values.filter(Boolean))].sort().join('\u0000'); }
function normalizeCssValue(value) { return String(value ?? '').trim().replace(/\s+/g, ' '); }
function cssIdentifier(value) { return String(value ?? 'unknown').replace(/[^A-Za-z0-9_-]/g, '-').replace(/^-+/, '') || 'unknown'; }
function compactRecord(record) { return Object.fromEntries(Object.entries(record).filter(([, value]) => value !== undefined)); }

export { autoDependencyGraphProofCandidates };
