function autoUrlAssetDependencyGraphProofCandidates({ sourcePath, input = {}, dependencyGraphEvidence, outputDependencyGraphEvidence, binding, hash }) {
  if (input.cssDependencyGraphAutoProofs === false || input.autoCssDependencyGraphProofs === false) return [];
  const result = [];
  for (const side of ['worker', 'head']) {
    const opposite = side === 'worker' ? 'head' : 'worker';
    const baseEvidence = dependencyGraphEvidence.sides?.base;
    const sideEvidence = dependencyGraphEvidence.sides?.[side];
    const oppositeEvidence = dependencyGraphEvidence.sides?.[opposite];
    if (!baseEvidence || !sideEvidence || !oppositeEvidence || !outputDependencyGraphEvidence) continue;
    const changedSurfaces = (dependencyGraphEvidence.changedDependencySurfaces ?? [])
      .filter((change) => change.side === side && urlAssetChangeCanAutoProve(change));
    for (const change of changedSurfaces) {
      const rename = urlAssetRenameFromChange(change);
      if (!rename) continue;
      if (!oppositePreservesUrlAssetDependency(baseEvidence, oppositeEvidence, change.cascadeKey)) continue;
      if (!sidePreservesUrlAssetRename(baseEvidence, sideEvidence, change.cascadeKey, rename)) continue;
      if (!outputPreservesUrlAssetRename(sideEvidence, outputDependencyGraphEvidence, change.cascadeKey, rename)) continue;
      result.push(compactRecord({
        id: `auto_css_url_asset_${side}_${safeId(rename.from)}_to_${safeId(rename.to)}`,
        kind: 'css-source-bound-dependency-graph-proof',
        status: 'passed',
        proofLevel: 'css-url-asset-source-bound',
        sourcePath,
        reasonCode: 'css-dependency-graph-proof-unproved',
        side,
        cascadeKey: change.cascadeKey,
        ...sourceHashRecord(binding, hash),
        dependencyGraphHashes: compactRecord({
          base: baseEvidence.dependencyGraphHash,
          worker: dependencyGraphEvidence.sides?.worker?.dependencyGraphHash,
          head: dependencyGraphEvidence.sides?.head?.dependencyGraphHash,
          output: outputDependencyGraphEvidence.dependencyGraphHash
        }),
        outputDependencyGraphHash: outputDependencyGraphEvidence.dependencyGraphHash,
        autoGenerated: true,
        sourceBound: true,
        urlAssetRename: {
          from: rename.from,
          to: rename.to,
          declarationCascadeKeys: [change.cascadeKey],
          browserCascadeEquivalenceClaim: false,
          browserRenderEquivalenceClaim: false,
          semanticEquivalenceClaim: false
        }
      }));
    }
  }
  return result;
}

function urlAssetChangeCanAutoProve(change) {
  if (!change.before?.dependencyKinds?.includes('url-asset-reference') || !change.after?.dependencyKinds?.includes('url-asset-reference')) return false;
  const rename = urlAssetRenameFromChange(change);
  if (!rename) return false;
  return cssUrlValueRenamedOnly(change.beforeValue, change.afterValue, rename.from, rename.to);
}

function urlAssetRenameFromChange(change) {
  const before = uniqueStrings(change.before?.names ?? []);
  const after = uniqueStrings(change.after?.names ?? []);
  if (before.length !== 1 || after.length !== 1 || before[0] === after[0]) return undefined;
  return { from: before[0], to: after[0] };
}

function oppositePreservesUrlAssetDependency(baseEvidence, oppositeEvidence, cascadeKey) {
  return stableList(urlAssetsForCascadeKey(baseEvidence, cascadeKey).map(urlAssetIdentity)) ===
    stableList(urlAssetsForCascadeKey(oppositeEvidence, cascadeKey).map(urlAssetIdentity));
}

function sidePreservesUrlAssetRename(baseEvidence, sideEvidence, cascadeKey, rename) {
  const baseAssets = urlAssetsForCascadeKey(baseEvidence, cascadeKey);
  const sideAssets = urlAssetsForCascadeKey(sideEvidence, cascadeKey);
  if (!baseAssets.some((entry) => entry.url === rename.from) || sideAssets.some((entry) => entry.url === rename.from)) return false;
  return stableList(baseAssets.map((entry) => entry.url === rename.from ? rename.to : entry.url)) ===
    stableList(sideAssets.map((entry) => entry.url));
}

function outputPreservesUrlAssetRename(sideEvidence, outputEvidence, cascadeKey, rename) {
  const sideAssets = urlAssetsForCascadeKey(sideEvidence, cascadeKey);
  const outputAssets = urlAssetsForCascadeKey(outputEvidence, cascadeKey);
  if (outputAssets.some((entry) => entry.url === rename.from)) return false;
  return stableList(sideAssets.map(urlAssetWithoutHashIdentity)) ===
    stableList(outputAssets.map(urlAssetWithoutHashIdentity));
}

function urlAssetsForCascadeKey(evidence, cascadeKey) {
  return (evidence.records?.urlAssetReferences ?? []).filter((entry) => entry.cascadeKey === cascadeKey);
}

function urlAssetIdentity(entry) {
  return [entry.cascadeKey, entry.property, entry.url, entry.declarationHash].join('::');
}

function urlAssetWithoutHashIdentity(entry) {
  return [entry.cascadeKey, entry.property, entry.url].join('::');
}

function cssUrlValueRenamedOnly(beforeValue, afterValue, from, to) {
  if (typeof beforeValue !== 'string' || typeof afterValue !== 'string') return false;
  return normalizeCssValue(beforeValue).replace(normalizeCssUrlToken(from), normalizeCssUrlToken(to)) === normalizeCssValue(afterValue);
}

function normalizeCssUrlToken(value) {
  return String(value ?? '').trim().replace(/^['"]|['"]$/g, '');
}

function sourceHashRecord(binding = {}, hash) {
  return compactRecord({
    baseSourceHash: hash?.(binding.base),
    workerSourceHash: hash?.(binding.worker),
    headSourceHash: hash?.(binding.head),
    outputSourceHash: hash?.(binding.output)
  });
}

function uniqueStrings(values = []) { return [...new Set(values.filter((value) => typeof value === 'string' && value.length > 0))]; }
function stableList(values = []) { return [...new Set(values.filter(Boolean))].sort().join('\u0000'); }
function normalizeCssValue(value) { return String(value ?? '').trim().replace(/\s+/g, ' ').replace(/url\(\s*(['"]?)(.*?)\1\s*\)/g, 'url($2)'); }
function safeId(value) { return String(value ?? 'unknown').replace(/[^A-Za-z0-9_-]/g, '-').replace(/^-+/, '') || 'unknown'; }
function compactRecord(record) { return Object.fromEntries(Object.entries(record).filter(([, value]) => value !== undefined)); }

export { autoUrlAssetDependencyGraphProofCandidates };
