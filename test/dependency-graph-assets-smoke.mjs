import assert from 'node:assert/strict';
import { hashSemanticValue } from '@shapeshift-labs/frontier-lang-kernel';
import { parseCssSemanticSheet, safeMergeCssSource } from '../dist/index.js';

const graphHash = (sourceText, sourcePath) => parseCssSemanticSheet(sourceText, { sourcePath }).dependencyGraphEvidence.dependencyGraphHash;

const fontFaceRenameBase = [
  '@font-face { font-family: Inter; src: url("./inter.woff2"); font-weight: 400; }',
  '.copy { font-family: Inter, sans-serif; color: red; }',
  ''
].join('\n');
const fontFaceRenameWorker = fontFaceRenameBase
  .replace('font-family: Inter; src', 'font-family: InterNext; src')
  .replace('font-family: Inter, sans-serif', 'font-family: InterNext, sans-serif');
const fontFaceRenameHead = fontFaceRenameBase.replace('color: red', 'color: blue');
const fontFaceRenameOutput = '@font-face { font-family: InterNext; src: url("./inter.woff2"); font-weight: 400; }\n\n.copy {\n  font-family: InterNext, sans-serif;\n  color: blue;\n}\n';
const fontFaceRenameMerge = safeMergeCssSource({
  id: 'css_font_face_family_auto_proof',
  sourcePath: 'fonts.css',
  baseSourceText: fontFaceRenameBase,
  workerSourceText: fontFaceRenameWorker,
  headSourceText: fontFaceRenameHead
});
assert.equal(fontFaceRenameMerge.status, 'merged');
assert.equal(fontFaceRenameMerge.mergedSourceText, fontFaceRenameOutput);
assert.equal(fontFaceRenameMerge.browserCascadeEquivalenceClaim, false);
assert.equal(fontFaceRenameMerge.dependencyGraphProofs.length, 1);
assert.equal(fontFaceRenameMerge.cascadeRuntimeProofs.length, 0);
assert.equal(fontFaceRenameMerge.dependencyGraphProofs[0].proofLevel, 'css-font-face-family-source-bound');
assert.equal(fontFaceRenameMerge.dependencyGraphProofs[0].autoGenerated, true);
assert.equal(fontFaceRenameMerge.dependencyGraphProofs[0].fontFaceRename.from, 'Inter');
assert.equal(fontFaceRenameMerge.dependencyGraphProofs[0].fontFaceRename.to, 'InterNext');
assert.deepEqual(fontFaceRenameMerge.dependencyGraphProofs[0].fontFaceRename.declarationCascadeKeys, ['.copy::font-family']);
assert.equal(fontFaceRenameMerge.dependencyGraphProofs[0].coveredSourceShapeChanges.length, 1);
assert.equal(fontFaceRenameMerge.admission.cssDependencyGraphProofs[0].fontFaceRename.browserCascadeEquivalenceClaim, false);

const partialFontFaceRename = safeMergeCssSource({
  id: 'css_font_face_family_partial_rename',
  sourcePath: 'fonts.css',
  baseSourceText: fontFaceRenameBase,
  workerSourceText: fontFaceRenameBase.replace('font-family: Inter; src', 'font-family: InterNext; src'),
  headSourceText: fontFaceRenameHead
});
assert.equal(partialFontFaceRename.status, 'blocked');
assert.equal(partialFontFaceRename.dependencyGraphProofs.length, 0);
assert.equal(partialFontFaceRename.conflicts.some((item) => item.details.reasonCode === 'css-font-face-runtime-equivalence-unproved'), true);

const staleManualFontFaceProof = safeMergeCssSource({
  id: 'css_font_face_family_stale_manual_proof',
  sourcePath: 'fonts.css',
  baseSourceText: fontFaceRenameBase,
  workerSourceText: fontFaceRenameBase.replace('font-family: Inter, sans-serif', 'font-family: InterNext, sans-serif'),
  headSourceText: fontFaceRenameHead,
  cssDependencyGraphAutoProofs: false,
  cssDependencyGraphProofs: [{
    id: 'proof_stale_font_face_family',
    kind: 'css-source-bound-dependency-graph-proof',
    status: 'passed',
    sourcePath: 'fonts.css',
    reasonCode: 'css-dependency-graph-proof-unproved',
    side: 'worker',
    cascadeKey: '.copy::font-family',
    baseSourceHash: hashSemanticValue(fontFaceRenameBase),
    workerSourceHash: hashSemanticValue(fontFaceRenameWorker),
    headSourceHash: hashSemanticValue(fontFaceRenameHead),
    outputSourceHash: hashSemanticValue(fontFaceRenameOutput),
    dependencyGraphHashes: { base: graphHash(fontFaceRenameBase, 'fonts.css'), worker: graphHash(fontFaceRenameWorker, 'fonts.css'), head: graphHash(fontFaceRenameHead, 'fonts.css') }
  }]
});
assert.equal(staleManualFontFaceProof.status, 'blocked');
assert.equal(staleManualFontFaceProof.dependencyGraphProofs.length, 0);
assert.equal(staleManualFontFaceProof.conflicts.some((item) => item.details.reasonCode === 'css-dependency-graph-proof-unproved'), true);

